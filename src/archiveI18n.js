export const isSupportedLocale = (locale) =>
  SUPPORTED_LOCALES.some((supportedLocale) => supportedLocale.id === locale);

export const getInitialLocale = () => {
  if (typeof window === "undefined") {
    return "en";
  }

  const storedLocale = window.localStorage.getItem(LOCALE_STORAGE_KEY);

  return isSupportedLocale(storedLocale) ? storedLocale : "en";
};

export const getNextLocale = (locale) => (locale === "zh" ? "en" : "zh");

export const getLocaleMeta = (locale) =>
  SUPPORTED_LOCALES.find((supportedLocale) => supportedLocale.id === locale) ??
  SUPPORTED_LOCALES[0];

const interpolate = (template, values) =>
  Object.entries(values).reduce(
    (result, [key, value]) => result.split(`{${key}}`).join(String(value)),
    template
  );

export const createTranslator =
  (locale) =>
  (key, values = {}) => {
    const localizedValue = TRANSLATIONS[locale]?.[key] ?? TRANSLATIONS.en[key] ?? key;

    return interpolate(localizedValue, values);
  };
